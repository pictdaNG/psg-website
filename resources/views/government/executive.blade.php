        <h3>THE GOVERNOR</h3>

        <p>The Governor is both the head of government and the Chief Security officer of the State. Under Article 5 Part II, second schedule of the Constitution, the executive powers is vested in the Governor of the State and, may subject as to the provisions of any law made by the House of Assembly, be exercised by him or through the Deputy Governor and Commissioners of the Government of the State or officers in the Public Service of the State. Ministries, each led by an appointed member of the Governor's Cabinet carry out the day-to-day administration of the state government. The Governor also appoints the heads of more than 38 extra-ministerial agencies, departments, Boards, parastatals etc.</p>

        <p>
            The Governor is limited to two four-year terms by the Constitution of the Federal Republic.  By tradition, the Governor and the First Family live in the Government House, also the location of the Office and the offices of the Deputy Governor and his senior staff.
        </p>

        <h3>THE DEPUTY GOVERNOR</h3>

        <p>
            The primary responsibility of the Deputy Governor is to be ready at a moment's notice to assume the Governorship if the Governor is unable to perform his duties. This can be because of the Governor's death, resignation, or permanent incapacitation, or if the Governor is removed from Office by the State House of Assembly. The duties of the Deputy Governor, outside of those enumerated in the Constitution, are at the discretion of the Governor. The Deputy Governor has an office and an official lodge attached to him.
        </p>

        <h3>OFFICE OF THE GOVERNOR</h3>
        <p>
            Every day, the State Governor is faced with scores of decisions, each with important consequences for the state’s future. To provide the Governor with the support that he needs to govern effectively, the Executive Office of the Governor has responsibility for tasks ranging from communicating the Governor’s message to the people and sundry responsibilities as the need arises, all in the interest of good and effective governance. This office is overseen by the Chief of Staff. The Office of the Governor also has in it Special Advisers, Senior Special Assistants and Special Assistants who are appointed with full discretion of the Governor. Although the Governor seeks the approval of the State House of Assembly on number of Special Advisers to be appointed, those nominated do not go through a screening exercise by the Assembly. The SAs, SSAs and SAs are appointed as each Governor identifies his needs and priorities.
        </p>

        <h3>THE CABINET</h3>
        <p>
            The Cabinet is an advisory body made up of the Commissioners of the State Ministries in Plateau State. Appointed by the Governor and confirmed by the House of Assembly, members of the Cabinet are the Governor's members of the State Executive Council. In addition to running the Ministries, the Commissioners represent the Governor at different fora as he mandates them. All the members of the Cabinet are Commissioner except for the Secretary to the State Government whose Office provides secretariat services and the Chief of Staff to the Governor who is also a member of cabinet and a few others who are in attendance as observers.
