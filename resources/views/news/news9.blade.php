    <h4>
      PROTOCOL
    </h4>
    <p>
      Today marks another very important day in the history of Plateau State as the team of Commissioners that will assist in delivering on our second term mandate is being inaugurated.
    </p>

    <p>
      Similarly, this event is holding few days after our electoral victory was affirmed by the Governorship Election Petition Tribunal. We thank God and dedicate this collective victory to the entire people of Plateau State. Let me reiterate my call to members of the opposition to join hands with us to move our state forward as there is no winner no vanquished. 
    </p>
    <p>
      Distinguished Ladies and Gentlemen, you recall that following the return of this administration for a second term in office, I made a solemn commitment that with politics and campaigns over, we must hit the ground running in the Next Level for greater productivity. 
    </p>
    <p>
      You will also recall that during the swearing in of the Secretary to the Government of the State and the Chief of Staff last month, I responded to the perceived delay in appointing Commissioners by explaining that I was being strategic and careful because this time, I am taking full responsibility for those that will eventually be selected to serve our people as Members of the State Executive Council.
    </p>
    <p>
      In line with relevant constitutional provisions for constituting the State Executive Council, in September 2019, I transmitted a list of twenty-three (23) Commissioner nominees to the Plateau State House of Assembly for screening and clearance. The Honourable House went into action promptly and all members were successfully screened and cleared without delay. I wish to commend the diligence and commitment of the Rt. Honourable Speaker and Honourable Members of the House in doing a thorough work, all in an effort to ensure the governance system operates smoothly.
    </p>
    <p>
      As soon as the Commissioner nominees were appointed and cleared by the Plateau State House of Assembly, we took a unique decision by approving a state-wide familiarization tour of the seventeen (17) Local Government Areas, cutting across the three geo-political zones of the State.
    </p>

    <p>
      The philosophy was to appreciate the Royal Fathers on behalf of the people who gave us the mandate to serve them and further receive their Royal and fatherly blessings as partial preparations for the effective take-off as Honourable Commissioners who have been called to serve the State
    </p>
    <p>
      Also, the tour provided the opportunity to interact and engage in a quasi-peer review with policy makers at the grass roots, which included the Local Government Chairmen and their Management Teams with the view to providing further insights into wholistic policy implementation and understand their vision, challenges and expectations. This will certainly shorten the learning curve after being sworn in.
    </p>
    <p>
      From the reports of the tour, I am glad that you had a very successful outing and in a better position to appreciate issues as you take informed decisions for the common good of the people and state.
    </p>
    <p>
      Now that you have been sworn in and taken your oath of office as Commissioners, you have been legally equipped to discharge your duties in your respective Ministries. Let me on behalf of the government and good people of Plateau State congratulate all of you and that you should see yourselves on this appointment as first amongst equals. This mandate however goes with huge responsibilities of service delivery to our people within the ambit of the policy thrust of the second phase of the Rescue Mission.
    </p>
    <p>
      Shortly after securing victory for another second term, I commissioned the famous Boston Consulting Group (BCG) to undertake the task of developing a five-year Development Plan for Plateau State, which will shape our final term in office. 
    </p>
    <p>
      After months of engagements with Ministries, Departments, Agencies, Local Government councils, the Economic Advisory Team and other stakeholders, they have since submitted their report to me.
    </p>
    <p>
      This document provides the roadmap for the implementation of our Next Level Rescue Agenda in serving our people effectively.
    </p>
    <p>
      All appointees are expected to study the document, as directed to enable them hit the ground running and stay on target in implementing our Partyâ€™s agenda and campaign promises.
    </p>
    <p>
      As Commissioners you are the highest Political Heads of your Ministry, Department and Agency as the case may be. Similarly, as Members of the State Executive Council, which is the highest policy formulation organ, you are expected to advice on technical or procedural matters towards informed decisions on developmental issues. 
    </p>
    <p>
      Above all, you should see yourselves as part of history in meeting up to the challenges ahead, towards ensuring a seamless governance.
    </p>
    <p>
      In achieving these, you are expected to ensure good synergy and working cooperation with your Permanent Secretaries, Directors and other support Staff in the machinery of government towards successful implementation of programmes and projects.
    </p>
    <p>
      Honourable Commissioners, with some of these charges, you will appreciate that the burdens on your shoulders are very heavy and your position is not for glamour, but of absolute responsibility. Your appointment is a patriotic call to duty, which should be devoid of betrayal of trust.
    </p>
    <p>
      Since I discouraged every lobby and influence to personally select you, I am going to drive you so hard and hold you to the highest standards as you carry out your assignments to ensure we achieve tangible results.
    </p>
    <p>
      I therefore expect nothing less than loyalty, diligence, commitment, probity and accountability to our development plans within the three pillar policies of the administration, namely;
      <ul>
        <li>Peace, Security and Good governance; </li>
        <li>Physical Infrastructural Development; and </li>
        <li>Sustainable Economic Rebirth. </li>
      </ul>
    </p>  

    <p>
      I will therefore not hesitate to reprimand, chastise, query and where necessary discipline by removal from office, any of you who goes contrary to the agenda of our Rescue Government and indeed extant public service rules.
    </p>
    <p>
      Certainly, to fulfil the challenges of leaving a lasting legacy, you need every support from all and sundry. However, without prejudice to your different affiliations and the right to demand for quality service and good governance, let me specially plead that relations, friends and party loyalists should not exert unnecessary pressures on your wards as these could easily derail them and have negative effects on their effective performances in service delivery.
    </p>
    <p>
      Your Excellences, Ladies and Gentlemen, you will agree with me that we have so much to do within the short time available to us. We are very ambitious in marketing Plateau to the World as the most favourable destination for investment and true home of peace and Tourism that it is.
    </p>
    <p>
      As we consolidate on the peace we have restored to Plateau State, we hope to generate more revenues to complete ongoing projects, continue implementing our legacy projects as well as create more jobs for our teeming youths and women.
    </p>
    <p>
      This is why we have engaged a reputable Firm of Professionals to not only advise us on how to increase our revenue base, but also work with our Revenue Generating Agencies through the process. I urge all and sundry to cooperate with these Agencies in accomplishing positive results.
    </p>
    <p>
      In same vain, we shall soon hold an Economic and Investment Summit, which will also assist us to change the trajectory of Plateau to an investment destination of choice.
    </p>
    <p>
      There are many critical infrastructure and services that require urgent attention that I expect the new Commissioners to immediately address.
    </p>
    <p>
      Among them are health facilities such as the Trauma Hospital in Riyom, which was abandoned, but our administration has virtually completed and equipped it. I expect that hospital to be operational by next month, while others in Kanke and Mabudi in Langtang South should be completed and operational before April next year. 
    </p>
    <p>
      While we work on completing ongoing road projects, I am directing the Ministry of Works to immediately implement a zero tolerance to pot holes in the State, particularly in our urban roads to prevent them from further damage as well as enhance the convenience of motorists.
    </p>
    <p>
      Waste management and refuse evacuation is also another area that requires urgent attention. I am directing the ministry of environment to immediately rid the Capital City of Jos and other urban areas of waste dumps in the interim, and also set in motion a concrete and sustainable waste disposal mechanisms that will involve all stakeholders and the private sector.
    </p>
    <p>
      In order to ensure consistency and faithful implementation of projects and programmes of government, the Project Monitoring and Result Delivery Unit to be headed by the Chief of Staff has been created in line with the recommendations of the State Strategic Development Plan. The unit is to ensure that the state gets value for money and to track the implementation of all Projects and Programmes.
    </p>
    <p>
      As part of our efforts to deepen transparency and ensure the prudent management of scarce resources, I have approved the setting up of a Liquidity Management Committee. This will ensure that budgetary provisions and approvals, efficient financial management and adequate project financing are built into our governance system. This is consistent with the best practices in line with our strategic plan.
    </p>
    <p>
      We must live up to expectation and work together with the public to trace bad influence amongst our youths for possible rehabilitation through the different Ministries, Departments and Agencies. 
    </p>
    <p>
      All stakeholders including Government Functionaries, Traditional Rulers, Religious Leaders, Party Leaders and Supporters, Women, Youths, Organized Labour and Civil Society, shall enjoy unlimited privilege of actively participating in this great enterprise of building a prosperous and progressive State. Since we are living in times of great changes, we must team up for purposeful development and progress.
    </p>
    <p>
      It is now my singular honour and privilege to announce the following Portfolios for the Commissioners:
    </p>
    <p>
      As you assume duty in your various Ministries, remember that everything you will be engaging in revolves around our three-point policy thrust and the Strategic Plan document of the Administration.
    </p>
    <p>
      This is why after the swearing in and taking over the Instruments of Office, you will proceed on training programme as was recently done for Permanent Secretaries and Directors. 
    </p>
    <p>
      The thrust of this programme is to enable you prepare your Medium Term Revenue Framework with specific action plan for each MDA. This will further form the input for the Policy Retreat that will come up in the third week of October.
    </p>
    <p>
      Finally, and once more, I congratulate you on your appointment as commissioners and Members of the State Executive Council and look forward to a memorable journey in the service of our people.
    </p>
    <p>
      <strong>Thank you for your attention. </strong> <br />
      <strong>God Bless Plateau State. </strong> <br />
      <strong>God Bless the Federal Republic of Nigeria.</strong> <br />
    </p>

    <p>
      <strong>Rt. Hon. (Dr.) SIMON BAKO LALONG</strong> <br />
      <strong>Executive Governor.</strong>
    </p>

